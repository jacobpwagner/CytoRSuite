#'   channels = "7-AAD-A"
#' )
#' @importFrom flowWorkspace gh_pop_get_gate gs_pop_get_gate
#' gs_pop_get_data gh_pop_get_data sampleNames gh_get_transformations 
#' gh_get_pop_paths gs_get_pop_paths gh_pop_get_data gs_pop_get_data
#'
